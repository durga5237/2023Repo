package JavaPractice;

public class Programs {

	public static void main(String[] args) {

		int i = 10;
		int k = 20;

		System.out.println("before swapping i and k values is " + i + "and " + k);
		int c = i;
		i = k;
		k = c;
		System.out.println("after swapping i and k values is " + i + "and " + k);

	}

}
